package com.kwon.ucf.today_menu;

public class MenuData {
    public String name;
    public String price;
    public String corner;
    public float score;
    public int hot;

    public MenuData(String name, String price, String corner, float score, int hot) {
        this.name = name;
        this.price = price;
        this.corner = corner;
        this.score = score;
        this.hot = hot;
    }
}
